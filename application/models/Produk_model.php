    function get_kode($kode_p){
        $q = $this->db->query("SELECT MAX(RIGHT(kode_produk,4)) AS kd_max FROM produk WHERE kode_produk like '".$kode_p."%'");
        $result = $q->result();
        $kd = "";
        if($q->num_rows()>0){
            foreach($q->result() as $k){
                $tmp = ((int)$k->kd_max)+1;
                $kd = $kode_p.sprintf("%04s", $tmp);
              
            }
        }else{
            $kd = $kode_p."0001";
        }
        return $kd;

    }
